#include <stdio.h>
/**
 * add - addition of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: the sum of a+b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: the result of a - b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication of 2 number
 * @a: first number
 * @b: second number
 *
 * Return: the result of a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: the result of a / b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - module of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
